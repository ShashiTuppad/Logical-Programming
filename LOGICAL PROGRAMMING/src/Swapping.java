
public class Swapping {

	public static void main(String[] args) {
		int a=100;
		int b=200;
		int temp=0;
		System.out.println("before swapping "+"a vlaue is "+a +" "+ "b value is "+ b);
		
		temp=a;
		a=b;
		b=temp;
		System.out.println("after swapping "+"a vlaue is "+a +" "+ "b value is "+ b);

	}

}
